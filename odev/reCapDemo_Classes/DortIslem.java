public class DortIslem {
    public int toplam(int sayi1, int sayi2) {
        return sayi1 + sayi2;
    }

    public int fark(int sayi1, int sayi2) {
        return sayi1 - sayi2;
    }

    public int carp(int sayi1, int sayi2) {
        return sayi1 * sayi2;
    }

    public int bol(int sayi1, int sayi2) {
        return sayi1 / sayi2;
    }
}
